package entities;

public class Department {
	
	private String name;
	
	public Department() {	//Construtor Vazio	
	}

	public Department(String name) { //Construtor com parametros		
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
	
	
	
	
	